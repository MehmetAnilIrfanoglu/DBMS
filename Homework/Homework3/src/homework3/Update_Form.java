    public boolean verifySpaces(){
        String fname = jTextField_Name.getText();
        
        String password = String.valueOf(jPasswordField_password.getPassword());
        String confirm_password = String.valueOf(jPasswordField_confirm_password.getPassword());
        String phone = jTextField_phone.getText();
        String email = jTextField_email.getText();
        
        if(fname.trim().equals("") || 
                
                password.trim().equals("") ||
                confirm_password.trim().equals("") ||
                phone.trim().equals("") ||
                email.trim().equals("")){
            JOptionPane.showMessageDialog(null, "There is/are empty fields.","Gap Error",2);
            return false;
        }
        else if(!password.equals(confirm_password)){
            JOptionPane.showMessageDialog(null, "Passwords are not matching","Password Error",2);
            return false;
            
        }
        else{
            return true;
        }
    }
    
    public boolean checkUser(String user){
        PreparedStatement st;
        ResultSet rs;
        boolean is_user = false;
        String query = "SELECT * FROM `users` WHERE `username`= ?";
        
        try {
            st = Connecting.getConnection().prepareStatement(query);
            st.setString(1,user);
            rs = st.executeQuery();
            if (rs.next()){
               is_user = true;
               JOptionPane.showMessageDialog(null, "User Exists! Try another Username.","User Conflict",2);
            }
            
        }
        catch(SQLException ex) {
            Logger.getLogger(Update_Form.class.getName()).log(Level.SEVERE,null,ex);
        }
        return is_user;
    }
